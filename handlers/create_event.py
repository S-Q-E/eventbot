@create_event_router.message(Form.description)
async def process_max_part(message: types.Message, state: FSMContext):
    await state.update_data(description=message.text)
    db = next(get_db())
    categories = db.query(Category).order_by(Category.name).all()
    db.close()

    if not categories:
        await message.answer("–ö–∞—Ç–µ–≥–æ—Ä–∏–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –¥–æ–±–∞–≤—å—Ç–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –ø–µ—Ä–µ–¥ —Å–æ–∑–¥–∞–Ω–∏–µ–º —Å–æ–±—ã—Ç–∏—è.")
        return
    builder = InlineKeyboardBuilder()
    for category in categories:
        builder.button(
            text=category.name,
            callback_data=f"event_cat_{category.id}"
        )
    builder.adjust(2)
    await message.answer(
        "–í—ã–±–µ—Ä–∏—Ç–µ –∫–∞—Ç–µ–≥–æ—Ä–∏—é —Å–æ–±—ã—Ç–∏—è:",
        reply_markup=builder.as_markup()
    )
    await state.set_state(Form.waiting_for_category)


@create_event_router.callback_query(Form.waiting_for_category)
async def adding_to_db(callback: types.CallbackQuery, state: FSMContext):
    cat_id = int(callback.data.split("_")[-1])
    await state.update_data(category_id=cat_id)
    event_data = await state.get_data()
    db = next(get_db())
    new_event = Event(
        name=event_data['name'],
        description=event_data['description'],
        address=event_data['address'],
        event_time=event_data['event_time'],
        max_participants=int(event_data['max_participants']),
        price=int(event_data['price']),
        category_id=int(event_data['category_id'])
    )
    db.add(new_event)
    db.commit()
    all_events_btn = InlineKeyboardButton(text="–ü—Ä–æ—Å–º–æ—Ç—Ä–µ—Ç—å –≤—Å–µ —Å–æ–±—ã—Ç–∏—è", callback_data="events_page_1")
    markup = InlineKeyboardMarkup(inline_keyboard=[[all_events_btn]])
    await callback.message.reply(f"‚òëÔ∏è‚òëÔ∏è‚òëÔ∏è–ì–æ—Ç–æ–≤–æ! –°–æ–±—ã—Ç–∏–µ <b>{new_event.name}</b> —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω–æ!", reply_markup=markup)
    await state.clear()
    db.close()
